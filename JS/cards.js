//Funcion 1
function toggleCardContent() {
    var card = document.querySelector('.js-toggle');
    var content = card.querySelector('.card__content');

    card.classList.toggle('active');

    if (card.classList.contains('active')) {
        content.style.transform = 'rotateX(0deg)';
    } else {
        content.style.transform = 'rotateX(-90deg)';
    }
}

var cardContent = document.querySelector('.card__content');
cardContent.addEventListener('click', toggleCardContent);

